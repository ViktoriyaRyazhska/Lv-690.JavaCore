package hm4;


public class Car {
	private int year;
	private double engine;
	
	public Car(int year, double engine) {
		super();
		this.year = year;
		this.engine = engine;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public double getEngine() {
		return engine;
	}

	public void setEngine(double engine) {
		this.engine = engine;
	} 

	
}
