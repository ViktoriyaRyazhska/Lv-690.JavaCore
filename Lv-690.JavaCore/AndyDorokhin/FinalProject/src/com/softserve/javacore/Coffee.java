package com.softserve.javacore;

public abstract class Coffee {
    protected int waterPerCup;
    protected int coffeePerCup;
    protected int milkPerCup;
    protected int moneyPerCup;

    public int getWaterPerCup() {
        return waterPerCup;
    }

    public int getCoffeePerCup() {
        return coffeePerCup;
    }

    public int getMilkPerCup() {
        return milkPerCup;
    }

    public int getMoneyPerCup() {
        return moneyPerCup;
    }
}
